let age = parseInt(prompt("Please enter your age:"));

if (age >= 25) {
  alert("You are 25 years old and up.");
} else {
  alert("You are 24 years old and below");
}

let num = parseInt(prompt("Please enter a number:"));

if (num < 100) {
  alert("You entered a number less than 100.");
} else {
  alert("The number you entered is greater than or equal to 100.");
}
